    private void bntThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntThemActionPerformed
    if(txtHovaten.getText().length()<=0||txtLuong.getText().length()<=0){
        JOptionPane.showMessageDialog(this, "Vui Lòng không để trống dữ liệu");
    
    }else{
        
// Lấy dữ liệu từ các trường văn bản
        String hoTen = txtHovaten.getText();
        double luong = Double.parseDouble(txtLuong.getText()); // Chuyển đổi dữ liệu lương từ chuỗi sang số

        // Thêm nhân viên mới vào danh sách
        list.add(new Staff(hoTen, luong));

        // Cập nhật bảng
        fillTable();
    }
    }//GEN-LAST:event_bntThemActionPerformed

    private void bntLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntLuuActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Chọn vị trí lưu");

        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            try {
                FileWriter fileWriter = new FileWriter(fileToSave);
                PrintWriter printWriter = new PrintWriter(fileWriter);

                for (Staff staff : list) {
                    printWriter.println(staff.getFullname() + "," + staff.getSalary());
                }

                printWriter.close();
                fileWriter.close();

                System.out.println("Dữ liệu đã được lưu vào tệp tin: " + fileToSave.getAbsolutePath());
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }


    }//GEN-LAST:event_bntLuuActionPerformed

    private void bntDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntDocActionPerformed
        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
        int result = fileChooser.showOpenDialog(this);
        if (result == javax.swing.JFileChooser.APPROVE_OPTION) {
            java.io.File selectedFile = fileChooser.getSelectedFile();
            try ( java.util.Scanner scanner = new java.util.Scanner(selectedFile)) {
                list.clear();
                while (scanner.hasNextLine()) {
                    String[] parts = scanner.nextLine().split(",");
                    if (parts.length == 2) {
                        String fullname = parts[0];
                        double salary = Double.parseDouble(parts[1]);
                        list.add(new Staff(fullname, salary));
                    }
                }
                fillTable();
                System.out.println("Dữ liệu đã được đọc từ tệp: " + selectedFile.getAbsolutePath());
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_bntDocActionPerformed

