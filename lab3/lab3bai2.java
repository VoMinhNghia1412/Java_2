    public void fillTable() {
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        for (Staff staff : list) {
            String salaryAsString = Double.toString(staff.getSalary());
            String fullname = staff.getFullname();
            model.addRow(new String[]{staff.getFullname(), salaryAsString});

        }
        model.fireTableDataChanged();
    }

