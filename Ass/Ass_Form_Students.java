    public void chooseImage() {
        JFileChooser chooser = new JFileChooser();
        int result = chooser.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            String filename = file.getAbsolutePath();

            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow != -1) {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setValueAt(filename, selectedRow, 6);
            }

           
            Uploadhinh(filename);
        }
    }

    void Uploadhinh(String hinh) {

        String imagePath = "D:\\Hinh_Java2\\" + hinh;

        ImageIcon imageIcon = new ImageIcon(imagePath);

        Image image = imageIcon.getImage();

        Image scaledImage = image.getScaledInstance(lbImg.getWidth(), lbImg.getHeight(), Image.SCALE_SMOOTH);

        ImageIcon scaledIcon = new ImageIcon(scaledImage);

        lbImg.setIcon(scaledIcon);
    }

    public void jTable1MouseClicked() {
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            if (model != null) {
                txtMasv.setText(model.getValueAt(selectedRow, 0).toString());
                txtHoten.setText(model.getValueAt(selectedRow, 1).toString());
                txtEmail.setText(model.getValueAt(selectedRow, 2).toString());
                txtSoDT.setText(model.getValueAt(selectedRow, 3).toString());

                String gioitinh = model.getValueAt(selectedRow, 4).toString();
                if (gioitinh.equals("Nam")) {
                    rdbNam.setSelected(true);
                } else {
                    rdbNu.setSelected(true);
                }
                txtDiachi.setText(model.getValueAt(selectedRow, 5).toString());

                String hinh = model.getValueAt(selectedRow, 6).toString();

                if (hinh != null && !hinh.isEmpty()) {
                    Uploadhinh(hinh);
                } else {
                    lbImg.setIcon(null);
                }
            }
        }
    }

