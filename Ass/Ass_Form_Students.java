    private void bntUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntUpdateActionPerformed
        String masv = txtMasv.getText();
        String hoten = txtHoten.getText();
        String email = txtEmail.getText();
        String sodt = txtSoDT.getText();
        boolean gioitinh = rdbNam.isSelected();
        String diachi = txtDiachi.getText();
        String hinh = lbImg.getIcon() != null ? lbImg.getIcon().toString() : "";

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để cập nhật.");
            return;
        }

        StudentsDAO.updateStudent((DefaultTableModel) jTable1.getModel(), masv, hoten, email, sodt, gioitinh, diachi, hinh, selectedRow);
    }//GEN-LAST:event_bntUpdateActionPerformed

