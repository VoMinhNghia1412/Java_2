public class Books {
    int id ;
    String title ;
    float price ;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public Books(int id, String title, float price) {
        this.id = id;
        this.title = title;
        this.price = price;
    }

    public Books() {
    }
}
